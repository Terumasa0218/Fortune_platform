export function interpretVedicChart(params: {
  sunRashi: Rashi;
  moonRashi: Rashi;
  moonNakshatra: Nakshatra;
  ascendantRashi?: Rashi;
}): VedicReading {
  const base = RASHI_INTERPRETATION[params.sunRashi];
  const nakshatraSupplement = `月のナクシャトラ「${params.moonNakshatra}」：${NAKSHATRA_TALENT_SUPPLEMENT[params.moonNakshatra]}`;

  return {
    ...params,
    personality: base.personality,
    talent: `${base.talent}\n\n${nakshatraSupplement}`,
    destiny: base.destiny,
    loveStyle: base.loveStyle,
  };
}
